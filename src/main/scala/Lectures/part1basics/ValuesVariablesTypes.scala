package Lectures.part1basics

object ValuesVariablesTypes extends App{

  //Values
  
  val x : Int = 77
  println(x)

  val y : String= "Chandusiriyala"
  print(y)

  val b: Boolean = true
  println(b)
  
  val aFloat : Float = 7.7f
  println(aFloat)

  val aDouble: Double = 7.7777
  println(aDouble)

  val aLong: Long = 77425961929L
  println(aLong)
  
  
  //Variables  
  
  var xInt = 69
  xInt += 1
  println(xInt)
  
  //All rest are same as above 
  
  //Values are immutable 
  
  //Varibles are mutable
  
  
  


}
